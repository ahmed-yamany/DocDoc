
import Foundation

/// A namespace for the design system, organizing foundational elements and reusable tokens.
///
/// The `DesignSystem` structure provides a clear separation between:
/// - **Foundations**: Core design primitives (typography, colors, spacing, etc.).
/// - **Tokens**: Reusable design values for consistent theming.
public enum DesignSystem {
    
    /// A collection of fundamental design elements.
    ///
    /// Includes measurements, typography, and other core building blocks.
    public enum Foundations {}

    /// A collection of reusable design tokens.
    ///
    /// Tokens represent predefined values for colors, typography, spacing, and other styling aspects
    /// that ensure consistency across the app.
    public enum Tokens {}
}

